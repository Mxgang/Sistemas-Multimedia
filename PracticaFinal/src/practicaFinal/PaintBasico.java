    /**
     * Método para obtener las bandas de color de una imagen
     * 
     * @param srcImage imagen de origen
     * @param band valor de la banda
     * @return devuelve un nuevo BufferedImagen
     */
    public BufferedImage getImageBand(BufferedImage srcImage, int band) {
        ColorSpace cs = ColorSpace.getInstance(ColorSpace.CS_GRAY);
        ComponentColorModel cm = new ComponentColorModel(cs, false, false,Transparency.OPAQUE,DataBuffer.TYPE_BYTE);
        int bandList[] = {band};
        WritableRaster bandRaster = (WritableRaster) srcImage.getRaster().createWritableChild(0, 0,srcImage.getWidth(), srcImage.getHeight(), 0, 0, bandList);
        return new BufferedImage(cm, bandRaster, false, null);
    }
    
   
   
    
