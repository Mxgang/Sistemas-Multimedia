    /**
     * Función lookUp nueva, cuya operación es la famosa función aúrea
     * @param w valor PI
     * @param x valor aúreo
     * @return devuelve un LookupTable
     */
    public LookupTable aureo(double w, double x){

            byte[] lt = new byte[256];

            for (int l = 0; l <= 255; ++l) {
                lt[l] = (byte) ((w+x)/w-l);
            }
            ByteLookupTable slt = new ByteLookupTable(0, lt);
            return slt;
    }
