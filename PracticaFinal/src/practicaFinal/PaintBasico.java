    /**
     * Método que calcula el seno para la operación sinoidal
     * @param valor valor seno
     * @return devuelve un LookupTable
     */
    public LookupTable seno(double valor){
        
        valor = Math.toRadians(valor);
        double resultado = 255.0 / Math.sin(Math.toRadians(90.0));
        
        byte[] lt = new byte[256];
        for (int i = 0; i <= 255; ++i) {
            lt[i] = (byte) (resultado * Math.abs(Math.sin(Math.toRadians((double) i * valor))));
        }
        ByteLookupTable slt = new ByteLookupTable(0, lt);
        return slt;
    }
