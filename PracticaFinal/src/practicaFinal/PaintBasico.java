    /**
     * Método para rotar la imagen pasando por parámetro unos grados determinados
     * @param imagen imagen origen
     * @param grados grados que quieres que rote
     * @return 
     */
    private BufferedImage imgRotation(BufferedImage imagen, int grados) {
        //Rotación de la imagen tanto libre como un numero determinado
        double ancho = imagen.getWidth();
        double alto = imagen.getHeight();
        
        double radio = Math.toRadians(grados);
        double coseno = Math.abs(Math.cos(radio));
        double seno = Math.abs(Math.sin(radio));


        Point p1 = new Point((int) (ancho * coseno + alto * seno), (int) (ancho * seno + alto * coseno));
        Point p2 = new Point();
        
        BufferedImage out = new BufferedImage(p1.x, p1.y, imagen.getType());

        Graphics2D g2 = out.createGraphics();
        
        g2.setPaint(Color.WHITE);
        g2.fillRect(0, 0, p1.x, p1.y);

        p2.setLocation(p1);
        p1.setLocation(p1.x / 2, p1.y / 2);

        AffineTransform at = AffineTransform.getRotateInstance(radio, p1.x, p1.y);
        p1.setLocation((p2.x - ancho) / 2, (p2.y - alto) / 2);
        at.translate(p1.x, p1.y);

        AffineTransformOp atop;
        atop = new AffineTransformOp(at, AffineTransformOp.TYPE_BILINEAR);

        return atop.filter(imagen, null);
    }
